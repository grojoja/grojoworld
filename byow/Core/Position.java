package byow.Core;

public class Position {
    private int x;;
    private int y;

    public Position(int xCoord, int yCoord) {
        this.x = xCoord;
        this.y = yCoord;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
